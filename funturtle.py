import turtle
turtle.shape('turtle')
square=turtle.clone()
square.penup()
square.shape('square')
square.goto(100,100)
square.pendown()
square.stamp()
square.goto(190,100)
square.stamp()
square.goto(190,190)
square.stamp()
square.goto(100,190)
square.stamp()
square.goto(100,100)
triangle=turtle.clone()
turtle.hideturtle()
triangle.shape('triangle')
triangle.goto(50,0)
triangle.stamp()
triangle.goto(25,50)
triangle.stamp()
triangle.goto(0,0)
triangle.stamp()
square.penup()
square.goto(-200,125)
square.stamp()
square.goto(-200,75)
triangle.penup()
triangle.goto(-150,125)
triangle.stamp()
triangle.goto(-150,75)
turtle.mainloop()
